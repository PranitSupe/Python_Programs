## Comparison Operators

#1 Check Equal
a=10
b=10
print("equal:",a==b)

print("-------------------")

#2 Check Not Equal
a=10
b=10
print("not equal:",a!=b)

print("-------------------")


#3 Greater Than
a=10
b=5
print("greater than:",a>b)

print("-------------------")



#4 Less Than
a=10
b=5
print("less than:",a<b)
print("-------------------")

#5 Greater than or equal to
a=10
b=5
print("greater than or equal to:",a>=b)

print("-------------------")



#6 Less than or equal to
a=10
b=5
print("less than or equal to:",a<=b)
print("-------------------")

