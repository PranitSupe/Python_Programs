##Conditional statements:

#1. Even or Odd:
num=int(input("enter a number:"))
if (num%2==0):
    print(num,"is an even number")
else:
    print(num,"is an odd number")

#2. Positive or Negative:

num=int(input("enter a number:"))
if (num>=0):
    print(num,"is a positive number")
else:
    print(num,"is a negative number")